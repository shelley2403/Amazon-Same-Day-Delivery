    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        //int selectedRow = orderjTable.getSelectedRow();
        int selectRow = workRequestJTable.getSelectedRow();
        
        if (selectRow < 0) {
            JOptionPane.showMessageDialog(null, "Select a Row First");
            return;
        }
        PackagingRequest supRequest = (PackagingRequest)workRequestJTable.getValueAt(selectRow, 2);
        for (OrderItem item : supRequest.getOrder().getOrderItemList()){
            if (item.getEnterprise().equals(enterprise)){
        Enterprise e = item.getEnterprise();
        DeliveryManagementOrganization dmo = null;
        for (Organization org : e.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof DeliveryManagementOrganization) {
                dmo = (DeliveryManagementOrganization) org;

                DeliveryRequest delRequest = new DeliveryRequest();
                delRequest.setSender(userAccount);
                delRequest.setRequestDate(supRequest.getRequestDate());
                delRequest.setProductId(item.getProduct().getProductId());
                
                delRequest.setQuantity(item.getQuantity());
                delRequest.setCustomer(supRequest.getCustomer());
                delRequest.setStatus("Packing Done, Prepare to Deliver");
                delRequest.setResult("Incomplete");
                delRequest.setProductName(item.getProduct().getName());
                delRequest.setOrder(supRequest.getOrder());
                dmo.getWorkQueue().getWorkRequestList().add(delRequest);
                userAccount.getWorkQueue().getWorkRequestList().add(delRequest);
                
            }
        }
            }
        }
        JOptionPane.showMessageDialog(null, "Your order is sent to Delivery Department !!!");
        supRequest.setResult("Completed");
        supRequest.setStatus("Order Sent to Delivery");
        populateTable();
    }//GEN-LAST:event_submitJButtonActionPerformed

