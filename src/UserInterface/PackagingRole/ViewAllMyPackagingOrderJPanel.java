    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);

            PackagingOrganization pmo = null;
        for (Organization organization1 : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization1 instanceof PackagingOrganization) {
                pmo = (PackagingOrganization) organization1;

                for (WorkRequest request : pmo.getWorkQueue().getWorkRequestList()) {
                    Object[] row = new Object[8];
                    request.setReceiver(userAccount);
                    
                    row[0] = request.getSender().getEmployee().getName();
                    row[1] = request.getReceiver();
                    row[2] = ((PackagingRequest) request);
                    row[3] = ((PackagingRequest) request).getProductId();
                    row[4] = ((PackagingRequest) request).getQuantity();
                    row[5]=  request.getRequestDate();
                    row[6] = request.getStatus();
                    row[7]= ((PackagingRequest) request).getResult();
                    model.addRow(row);
                }
            }
        }
    }
        
    
